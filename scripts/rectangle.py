class Rectangle:
    id = 0
    x = 0
    y = 0
    w = 0
    h = 0
    firstFrame = 0
    lastFrame = 0

    def __intit__(self, i, x, y, w, h, ff, lf):
        self.id = id
        self.x = x
        self.y = y
        self.w = w
        self.h = h
        self.firstFrame = ff
        self.lastFrame - lf
